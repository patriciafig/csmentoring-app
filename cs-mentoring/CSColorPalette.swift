import UIKit

extension UIColor {
    //Returns a UIColor object representing the color SeaBlue #5AE1FC, whose RGB values are (90, 255, 252)
    class func SeaBlue (alpha: CGFloat = 1.0) -> UIColor {
        return UIColor.init(red: <#T##CGFloat#>, green: <#T##CGFloat#>, blue: <#T##CGFloat#>, alpha: <#T##CGFloat#>)
    }
    
    //Returns a UIColor object representing the color SkyBlue #2699FB, whose RGB values are (38, 153, 251)
